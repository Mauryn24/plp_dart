// Entry point of the program
void main() {
  // Define an integer variable 'number' and assign it the value 15
  int number = 15;
  // Check if 'number' is even or odd using the modulo operator (%)
  if (number % 2 == 0) {
    // If 'number' is even, print '15 is even.'
    print('$number is even.');
  } else {
    // If 'number' is odd, print '15 is odd.'
    print('$number is odd.');
  }
}
