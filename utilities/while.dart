// Entry point of the program
void main() {
  // Define an integer variable 'i' and initialize it with the value 20
  int i = 20;
  // Use a while loop to iterate while 'i' is greater than or equal to 10
  while (i >= 10) {
    // Print the current value of 'i'
    print(i);
    // Decrement the value of 'i' by 1
    i--;
  }
}
