// Define a function named 'sum' that takes two integer parameters 'a' and 'b'
int sum(int a, int b) {
  // Return the sum of 'a' and 'b'
  return a + b;
}

// Entry point of the program
void main() {
  // Call the 'sum' function with arguments 5 and 3 and print the result
  print(sum(5, 3)); // Output: 8
}
