void main() {
  // Integer data type
  int age = 25;

  // Double data type
  double temperature = 28.5;

  // String data type
  String name = 'John Doe';

  // List data type
  List<int> numbers = [1, 2, 3, 4, 5];

  // Map data type
  Map<String, dynamic> person = {
    'name': 'Alice',
    'age': 30,
    'isStudent': true
  };

  // Printing values
  print('Age: $age'); // Output: Age: 25
  print('Temperature: $temperature'); // Output: Temperature: 28.5
  print('Name: $name'); // Output: Name: John Doe
  print('Numbers: $numbers'); // Output: Numbers: [1, 2, 3, 4, 5]
  print('Person: $person'); // Output: Person: {name: Alice, age: 30, isStudent: true}
}
